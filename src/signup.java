    private void btn_okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_okActionPerformed
        try{
            String nama = txt_nama.getText();
            String username = txt_username.getText();
            String password = txt_password.getText();
            String jabatan = cmb_jabatan.getSelectedItem().toString();

            String sql = "insert into account (nama,username,password,jabatan) values('" + nama + "','" + 
                    username + "','" + password + "','" + jabatan + "')";
            st.executeUpdate(sql);
            JOptionPane.showMessageDialog(this, "Create account success");
            txt_nama.setText("");
            txt_username.setText("");
            txt_password.setText("");

            
        }catch(SQLException e) {
            JOptionPane.showMessageDialog(this, "Fail create account");
        }
  
    }//GEN-LAST:event_btn_okActionPerformed

